package com.example.basics

fun main(){
    print("Operadores Aritméticos en Historias Clínicas")
    val pesoPaciente: Double = 72.5
    val dosisPorKg: Double = 2.0

    println("Suma (peso + dosis): ${pesoPaciente + dosisPorKg}")
    println("Resta (peso - dosis): ${pesoPaciente - dosisPorKg}")
    println("Multiplicación (peso x dosis): ${pesoPaciente * dosisPorKg}")
    println("División (peso / dosis): ${pesoPaciente / dosisPorKg}")
}